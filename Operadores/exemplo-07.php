<?php

$a = 10;

echo ++$a. " pré incremento";//Pré-incremento (adiciona +1 antes de rodar esse comando)
echo "<br>";
echo $a++. " pós incremento";//Pós-incremento (adiciona +1 depois de rodar esse comando)

echo "<br>";

echo $a . " valor isolado pós incremento<br>";




$a = 10;

echo --$a;//Pré-decremento (adiciona -1 antes de rodar esse comando)
echo "<br>";
echo $a--;//Pós-decremento (adiciona -1 depois de rodar esse comando)

echo "<br>";

echo $a;


?>